const fs = require("fs");
const path = require("path");
const { execFile } = require("child_process");

const {
  docsRoot,
  repoRoot,
  generatedDocMaxAgeDays,
  humanDocReportAgeDays,
  normalizeSlashes,
  isHistoricalDoc,
  isPublishedDoc,
  publishedLandingPages,
  requiredPublishedFrontmatterDocs,
  futurePackageReferencePrefixes,
  roadmapIssueSourcePaths,
  roadmapIssueLookupTimeoutMs,
} = require("./docs-qa-config.cjs");

const LAST_REFRESHED_RE = /Last refreshed:\s*([0-9]{4}-[0-9]{2}-[0-9]{2})/i;
const LAST_UPDATED_RE = /Last updated:\s*([A-Za-z]+\s+[0-9]{4}|[0-9]{4}-[0-9]{2}-[0-9]{2})/i;
const GENERATED_RE = /Generated by/i;
const FRONTMATTER_RE = /^---\n([\s\S]*?)\n---\n?/;
const CLI_COMMAND_RE = /\b(babysitter(?:-harness)?)\s+([a-z][a-z0-9-]*(?::[a-z0-9-]+)?)/g;
const PACKAGE_REFERENCE_RE = /\B(@[a-z0-9-]+\/[a-z0-9-]+(?:\/[a-z0-9._-]+)*)/gi;
const VERSION_LABEL_RE = /(?:^\*\*Version:\*\*\s*(.+)$|^\*\*Specification\s+(v[0-9][^\n*]*)\*\*|^###\s+Version\s+([0-9][0-9A-Za-z.\-]*))/gim;
const HISTORICAL_CONTEXT_RE = /\b(removed|legacy|historical|archived|deprecated|superseded|old)\b/i;
const REQUIRED_FRONTMATTER_FIELDS = ["title", "description", "last_updated"];
const GITHUB_ISSUE_URL_RE = /https:\/\/github\.com\/([A-Za-z0-9_.-]+\/[A-Za-z0-9_.-]+)\/issues\/([0-9]+)/g;
const ISSUE_SHORTHAND_RE = /^([A-Za-z0-9_.-]+\/[A-Za-z0-9_.-]+)#([0-9]+)$/;

function listMarkdownFiles(targetPath) {
  const stat = fs.statSync(targetPath);
  if (stat.isFile()) {
    return targetPath.endsWith(".md") || targetPath.endsWith(".mdx") ? [targetPath] : [];
  }

  return fs
    .readdirSync(targetPath, { withFileTypes: true })
    .flatMap((entry) => {
      if (entry.name === "node_modules" || entry.name === ".git") {
        return [];
      }
      return listMarkdownFiles(path.join(targetPath, entry.name));
    })
    .sort((left, right) => left.localeCompare(right));
}

function parseFlexibleDate(value) {
  const parsed = Date.parse(value.length === 8 ? `${value}-01` : value);
  return Number.isNaN(parsed) ? null : parsed;
}

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf8"));
}

function listPackageJsonFiles(targetPath) {
  const entries = fs.readdirSync(targetPath, { withFileTypes: true });
  const results = [];

  for (const entry of entries) {
    if (
      entry.name === "node_modules" ||
      entry.name === ".git" ||
      entry.name === "dist" ||
      entry.name === "build" ||
      entry.name === ".next"
    ) {
      continue;
    }

    const entryPath = path.join(targetPath, entry.name);
    if (entry.isFile() && entry.name === "package.json") {
      results.push(entryPath);
      continue;
    }

    if (entry.isDirectory()) {
      results.push(...listPackageJsonFiles(entryPath));
    }
  }

  return results.sort((left, right) => left.localeCompare(right));
}

function collectKnownPackages() {
  const names = new Set();

  for (const packageJsonPath of listPackageJsonFiles(repoRoot)) {
    const packageJson = readJson(packageJsonPath);
    for (const key of ["name"]) {
      if (typeof packageJson[key] === "string") {
        names.add(packageJson[key]);
      }
    }

    for (const dependencyKey of ["dependencies", "devDependencies", "peerDependencies", "optionalDependencies"]) {
      const dependencyMap = packageJson[dependencyKey];
      if (!dependencyMap || typeof dependencyMap !== "object") {
        continue;
      }
      for (const dependencyName of Object.keys(dependencyMap)) {
        names.add(dependencyName);
      }
    }
  }

  return Array.from(names).sort((left, right) => left.localeCompare(right));
}

function collectValidCommands(programSource) {
  const commands = new Set();
  const matches = programSource.matchAll(/"([a-z][a-z0-9-]*(?::[a-z0-9-]+)?)"/g);
  for (const match of matches) {
    commands.add(match[1]);
  }
  return commands;
}

function parseFrontmatter(text) {
  const match = text.match(FRONTMATTER_RE);
  if (!match) {
    return null;
  }

  const data = {};
  for (const rawLine of match[1].split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith("#")) {
      continue;
    }
    const separatorIndex = line.indexOf(":");
    if (separatorIndex <= 0) {
      continue;
    }
    const key = line.slice(0, separatorIndex).trim();
    const value = line.slice(separatorIndex + 1).trim().replace(/^['"]|['"]$/g, "");
    data[key] = value;
  }

  return data;
}

function resolveLineNumber(text, matchIndex) {
  return text.slice(0, matchIndex).split(/\r?\n/).length;
}

function isKnownPackageReference(reference, knownPackages) {
  if (knownPackages.has(reference)) {
    return true;
  }

  for (const knownPackage of knownPackages) {
    if (reference.startsWith(`${knownPackage}/`)) {
      return true;
    }
  }

  return false;
}

function shouldInspectCliLine(line) {
  const trimmed = line.trim();
  return (
    trimmed.startsWith("babysitter ") ||
    trimmed.startsWith("babysitter-harness ") ||
    trimmed.includes("CLI=\"babysitter") ||
    trimmed.includes("CLI='babysitter") ||
    trimmed.includes("run: babysitter ") ||
    trimmed.includes("run: babysitter-harness ") ||
    trimmed.includes("shell(\"babysitter ") ||
    trimmed.includes("shell('babysitter ")
  );
}

function getCliCandidatesFromLine(line) {
  const candidates = [];
  const trimmed = line.trim();

  for (const match of line.matchAll(/`([^`]+)`/g)) {
    if (/\bbabysitter(?:-harness)?\s+[a-z]/.test(match[1])) {
      candidates.push(match[1]);
    }
  }

  if (shouldInspectCliLine(line) && /\bbabysitter(?:-harness)?\s+[a-z]/.test(trimmed)) {
    candidates.push(line);
  }

  return candidates;
}

function normalizeIssueReference(value) {
  const trimmed = String(value ?? "").trim().replace(/^[\s"'`(<]+|[\s"'`),.>]+$/g, "");
  const shorthand = trimmed.match(ISSUE_SHORTHAND_RE);
  if (shorthand) {
    return `${shorthand[1]}#${shorthand[2]}`;
  }

  const url = trimmed.match(/^https:\/\/github\.com\/([A-Za-z0-9_.-]+\/[A-Za-z0-9_.-]+)\/issues\/([0-9]+)(?:[#?].*)?$/);
  if (url) {
    return `${url[1]}#${url[2]}`;
  }

  return null;
}

function getMarkdownTableItem(line) {
  const trimmed = line.trim();
  if (!trimmed.startsWith("|") || !trimmed.endsWith("|")) {
    return null;
  }
  const cells = trimmed
    .slice(1, -1)
    .split("|")
    .map((cell) => cell.trim());
  const firstCell = cells[0];
  if (!firstCell || /^-+$/.test(firstCell)) {
    return null;
  }
  return firstCell.replace(/^\[([^\]]+)\]\([^)]+\)$/, "$1");
}

function parseRoadmapIssueLinks({ file, text }) {
  const links = [];
  const seen = new Set();
  const lines = String(text ?? "").split(/\r?\n/);
  let currentItem = null;

  function addLink(issue, line, item) {
    const key = `${file}:${issue}`;
    if (seen.has(key)) {
      return;
    }
    seen.add(key);
    links.push({
      file,
      line,
      issue,
      item: item || issue,
    });
  }

  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index];
    const lineNumber = index + 1;
    const idMatch = line.match(/^\s*-\s*id:\s*([A-Za-z0-9_.-]+)/) || line.match(/\bid:\s*([A-Za-z0-9_.-]+)/);
    if (idMatch) {
      currentItem = idMatch[1];
    }

    const issueLinkMatch = line.match(/\bissue_link:\s*(\S+)/);
    if (issueLinkMatch) {
      const issue = normalizeIssueReference(issueLinkMatch[1]);
      if (issue) {
        addLink(issue, lineNumber, currentItem);
      }
    }

    for (const match of line.matchAll(GITHUB_ISSUE_URL_RE)) {
      const issue = `${match[1]}#${match[2]}`;
      const markdownText = line.slice(0, match.index).match(/\[([^\]]+)\]\([^\)]*$/);
      addLink(issue, lineNumber, markdownText?.[1] || getMarkdownTableItem(line) || currentItem);
    }
  }

  return links;
}

function classifyRoadmapIssueState(issueState) {
  const state = String(issueState?.state ?? "").toUpperCase();
  const stateReason = issueState?.stateReason == null ? null : String(issueState.stateReason).toUpperCase();

  if (state === "CLOSED" && stateReason === "NOT_PLANNED") {
    return "not_planned";
  }
  if (state === "CLOSED" && stateReason == null) {
    return "unknown";
  }
  return "not_stale";
}

async function lookupIssueWithGh(issue, { timeoutMs = roadmapIssueLookupTimeoutMs, execFileImpl = execFile } = {}) {
  return new Promise((resolve, reject) => {
    execFileImpl(
      "gh",
      ["issue", "view", issue, "--json", "state,stateReason,closedAt,url,title"],
      { timeout: timeoutMs },
      (error, stdout) => {
        if (error) {
          reject(error);
          return;
        }
        try {
          resolve(JSON.parse(stdout));
        } catch (parseError) {
          reject(parseError);
        }
      },
    );
  });
}

async function collectRoadmapIssueWarnings({ roadmapSources, lookupIssue = lookupIssueWithGh } = {}) {
  const linksByIssue = new Map();

  for (const source of roadmapSources ?? []) {
    for (const link of parseRoadmapIssueLinks(source)) {
      if (!linksByIssue.has(link.issue)) {
        linksByIssue.set(link.issue, link);
      }
    }
  }

  const warnings = [];
  for (const [issue, link] of linksByIssue) {
    let issueState;
    try {
      issueState = await lookupIssue(issue);
    } catch (error) {
      continue;
    }

    if (classifyRoadmapIssueState(issueState) !== "not_planned") {
      continue;
    }

    warnings.push({
      severity: "warning",
      file: link.file,
      line: link.line,
      item: link.item,
      issue,
      title: issueState.title ?? null,
      url: issueState.url ?? null,
      message: `${link.item} links to ${issue}, which GitHub reports as CLOSED/NOT_PLANNED; consider dropping or revising the roadmap item.`,
    });
  }

  return warnings;
}

async function collectRoadmapIssueReport(options = {}) {
  const roadmapIssueWarnings = await collectRoadmapIssueWarnings(options);
  return {
    exitCode: 0,
    roadmapIssueWarnings,
  };
}

function readRoadmapSources() {
  return roadmapIssueSourcePaths
    .map((relativePath) => {
      const absolutePath = path.join(repoRoot, relativePath);
      if (!fs.existsSync(absolutePath)) {
        return null;
      }
      return {
        file: relativePath,
        text: fs.readFileSync(absolutePath, "utf8"),
      };
    })
    .filter(Boolean);
}

async function main() {
  const artifactDir = path.join(repoRoot, "artifacts", "docs-qa");
  fs.mkdirSync(artifactDir, { recursive: true });

  const now = Date.now();
  const validCommands = collectValidCommands(
    fs.readFileSync(path.join(repoRoot, "packages", "sdk", "src", "cli", "main", "program.ts"), "utf8"),
  );
  const knownPackages = new Set(collectKnownPackages());
  // External plugin packages — derived from Atlas PluginTarget records
  const externalPluginPackages = [];
  try {
    const { listPluginTargetDescriptors } = require(path.join(repoRoot, "packages", "agent-catalog", "dist", "sdk.js"));
    for (const target of listPluginTargetDescriptors()) {
      externalPluginPackages.push(`@a5c-ai/babysitter-${target.adapterName === "omp" ? "omp" : target.targetId}`);
    }
  } catch {
    // Fallback if catalog not built
    externalPluginPackages.push(
      "@a5c-ai/babysitter-codex", "@a5c-ai/babysitter-cursor", "@a5c-ai/babysitter-gemini",
      "@a5c-ai/babysitter-github", "@a5c-ai/babysitter-omp", "@a5c-ai/babysitter-openclaw",
      "@a5c-ai/babysitter-opencode", "@a5c-ai/babysitter-pi",
    );
  }
  for (const ext of externalPluginPackages) {
    knownPackages.add(ext);
  }
  const generatedFailures = [];
  const generatedReports = [];
  const humanReports = [];
  const metadataFailures = [];
  const invalidCliReferences = [];
  const invalidPackageReferences = [];
  const plannedPackageReferences = [];
  const roadmapIssueReport = await collectRoadmapIssueReport({
    roadmapSources: readRoadmapSources(),
  });
  const { roadmapIssueWarnings } = roadmapIssueReport;
  const versionClaims = [];
  const publishedDocInventory = [];
  const historicalDocInventory = [];

  for (const relativePath of requiredPublishedFrontmatterDocs) {
    const absolutePath = path.join(repoRoot, relativePath);
    if (!fs.existsSync(absolutePath)) {
      metadataFailures.push({
        file: relativePath,
        reason: "required published doc is missing",
      });
    }
  }

  for (const filePath of listMarkdownFiles(docsRoot)) {
    const relativePath = normalizeSlashes(path.relative(repoRoot, filePath));
    const text = fs.readFileSync(filePath, "utf8");
    const frontmatter = parseFrontmatter(text);
    const published = isPublishedDoc(relativePath);
    const historical = isHistoricalDoc(relativePath);
    const refreshedMatch = text.match(LAST_REFRESHED_RE);
    const updatedMatch = text.match(LAST_UPDATED_RE);
    const isGenerated = GENERATED_RE.test(text);

    if (published) {
      publishedDocInventory.push(relativePath);
    } else if (historical) {
      historicalDocInventory.push(relativePath);
    }

    if (isGenerated) {
      if (!refreshedMatch) {
        generatedFailures.push({
          file: relativePath,
          reason: "generated doc is missing a Last refreshed marker",
        });
        continue;
      }

      const refreshedAt = parseFlexibleDate(refreshedMatch[1]);
      if (refreshedAt == null) {
        generatedFailures.push({
          file: relativePath,
          reason: `invalid Last refreshed value: ${refreshedMatch[1]}`,
        });
        continue;
      }

      const ageDays = Math.floor((now - refreshedAt) / 86400000);
      generatedReports.push({
        file: relativePath,
        refreshedAt: refreshedMatch[1],
        ageDays,
      });

      if (ageDays > generatedDocMaxAgeDays) {
        generatedFailures.push({
          file: relativePath,
          reason: `generated doc is stale (${ageDays}d > ${generatedDocMaxAgeDays}d)`,
        });
      }
      continue;
    }

    if (updatedMatch) {
      const updatedAt = parseFlexibleDate(updatedMatch[1]);
      if (updatedAt != null) {
        const ageDays = Math.floor((now - updatedAt) / 86400000);
        if (ageDays > humanDocReportAgeDays) {
          humanReports.push({
            file: relativePath,
            updatedAt: updatedMatch[1],
            ageDays,
          });
        }
      }
    }

    if (published && requiredPublishedFrontmatterDocs.includes(relativePath)) {
      if (!frontmatter) {
        metadataFailures.push({
          file: relativePath,
          reason: "missing YAML frontmatter",
        });
      } else {
        for (const field of REQUIRED_FRONTMATTER_FIELDS) {
          if (typeof frontmatter[field] !== "string" || frontmatter[field].length === 0) {
            metadataFailures.push({
              file: relativePath,
              reason: `frontmatter is missing required field "${field}"`,
            });
          }
        }
      }
    }

    if (published && publishedLandingPages.includes(relativePath)) {
      if (!frontmatter) {
        metadataFailures.push({
          file: relativePath,
          reason: "landing page is missing YAML frontmatter",
        });
      } else if (frontmatter.category !== "landing") {
        metadataFailures.push({
          file: relativePath,
          reason: 'landing page frontmatter must set category: "landing"',
        });
      }
    }

    if (!published) {
      continue;
    }

    const lines = text.split(/\r?\n/);
    for (let lineIndex = 0; lineIndex < lines.length; lineIndex += 1) {
      const line = lines[lineIndex];
      const candidates = getCliCandidatesFromLine(line);
      if (candidates.length === 0) {
        continue;
      }

      for (const candidate of candidates) {
        for (const match of candidate.matchAll(CLI_COMMAND_RE)) {
          const binary = match[1];
          const command = match[2];
          if (binary !== "babysitter" && binary !== "babysitter-harness") {
            continue;
          }
          if (validCommands.has(command)) {
            continue;
          }
          if (!command.includes(":")) {
            continue;
          }
          if (HISTORICAL_CONTEXT_RE.test(line)) {
            continue;
          }

          invalidCliReferences.push({
            file: relativePath,
            line: lineIndex + 1,
            command,
          });
        }
      }
    }

    for (const match of text.matchAll(PACKAGE_REFERENCE_RE)) {
      const reference = match[1];
      if (!reference.startsWith("@a5c-ai/")) {
        continue;
      }
      if (isKnownPackageReference(reference, knownPackages)) {
        continue;
      }

      if (futurePackageReferencePrefixes.some((prefix) => relativePath.startsWith(prefix))) {
        plannedPackageReferences.push({
          file: relativePath,
          line: resolveLineNumber(text, match.index),
          packageName: reference,
        });
        continue;
      }

      invalidPackageReferences.push({
        file: relativePath,
        line: resolveLineNumber(text, match.index),
        packageName: reference,
      });
    }

    for (const match of text.matchAll(VERSION_LABEL_RE)) {
      const claim = match[1] || match[2] || match[3];
      if (!claim) {
        continue;
      }
      versionClaims.push({
        file: relativePath,
        line: resolveLineNumber(text, match.index),
        claim: claim.trim(),
      });
    }
  }

  const report = {
    generatedDocMaxAgeDays,
    humanDocReportAgeDays,
    generatedDocsChecked: generatedReports.length,
    staleGeneratedDocs: generatedFailures,
    staleHumanUpdatedDocs: humanReports,
    publishedDocsChecked: publishedDocInventory.length,
    historicalDocsExcluded: historicalDocInventory.length,
    landingPagesChecked: publishedLandingPages.length,
    metadataFailures,
    invalidCliReferences,
    invalidPackageReferences,
    plannedPackageReferences,
    roadmapIssueWarnings,
    versionClaims,
  };

  fs.writeFileSync(path.join(artifactDir, "freshness-report.json"), `${JSON.stringify(report, null, 2)}\n`);

  if (generatedFailures.length > 0) {
    console.error("[docs:freshness] stale generated documentation detected:");
    for (const failure of generatedFailures) {
      console.error(`- ${failure.file}: ${failure.reason}`);
    }
    process.exit(1);
  }

  if (metadataFailures.length > 0) {
    console.error("[docs:freshness] published docs metadata issues detected:");
    for (const failure of metadataFailures) {
      console.error(`- ${failure.file}: ${failure.reason}`);
    }
    process.exit(1);
  }

  if (invalidCliReferences.length > 0) {
    console.error("[docs:freshness] invalid babysitter CLI references detected:");
    for (const failure of invalidCliReferences) {
      console.error(`- ${failure.file}:${failure.line} references unknown command ${failure.command}`);
    }
    process.exit(1);
  }

  if (invalidPackageReferences.length > 0) {
    console.error("[docs:freshness] invalid package references detected:");
    for (const failure of invalidPackageReferences) {
      console.error(`- ${failure.file}:${failure.line} references unknown package ${failure.packageName}`);
    }
    process.exit(1);
  }

  if (humanReports.length > 0) {
    console.warn("[docs:freshness] older human-authored docs reported for staged cleanup:");
    for (const entry of humanReports) {
      console.warn(`- ${entry.file}: updatedAt=${entry.updatedAt}, ageDays=${entry.ageDays}`);
    }
  }

  if (versionClaims.length > 0) {
    console.warn("[docs:freshness] explicit version labels inventoried for review:");
    for (const entry of versionClaims.slice(0, 20)) {
      console.warn(`- ${entry.file}:${entry.line} -> ${entry.claim}`);
    }
    if (versionClaims.length > 20) {
      console.warn(`- ... ${versionClaims.length - 20} additional version labels recorded in artifacts/docs-qa/freshness-report.json`);
    }
  }

  if (plannedPackageReferences.length > 0) {
    console.warn("[docs:freshness] future-state package references inventoried for planning docs:");
    for (const entry of plannedPackageReferences.slice(0, 20)) {
      console.warn(`- ${entry.file}:${entry.line} -> ${entry.packageName}`);
    }
    if (plannedPackageReferences.length > 20) {
      console.warn(`- ... ${plannedPackageReferences.length - 20} additional planning references recorded in artifacts/docs-qa/freshness-report.json`);
    }
  }

  if (roadmapIssueWarnings.length > 0) {
    console.warn("[docs:freshness] roadmap items linked to NOT_PLANNED GitHub issues:");
    for (const warning of roadmapIssueWarnings) {
      console.warn(`- ${warning.file}:${warning.line} ${warning.message}`);
    }
  }

  console.log(JSON.stringify(report, null, 2));
}

module.exports = {
  parseRoadmapIssueLinks,
  classifyRoadmapIssueState,
  collectRoadmapIssueWarnings,
  collectRoadmapIssueReport,
  lookupIssueWithGh,
};

if (require.main === module) {
  main().catch((error) => {
    console.error(error);
    process.exit(1);
  });
}
